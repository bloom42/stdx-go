// UUIDString returns the UUID string form of GUID, xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx
func (guid GUID) ToUuidString() string {
	var dst [36]byte

	hex.Encode(dst[:], guid[:4])
	dst[8] = '-'
	hex.Encode(dst[9:13], guid[4:6])
	dst[13] = '-'
	hex.Encode(dst[14:18], guid[6:8])
	dst[18] = '-'
	hex.Encode(dst[19:23], guid[8:10])
	dst[23] = '-'
	hex.Encode(dst[24:], guid[10:])

	return string(dst[:])
}

// Parse decodes s into a GUID as an UUID or returns an error.
// The form shoud be: xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx
func ParseUuidString(input string) (guid GUID, err error) {
	if len(input) != 36 {
		err = ErrUuidIsNotValid
		return
	}
	// input must be of the form  xxxxxxxx-xxxx-xxxx-xxxx-xxxxxxxxxxxx
	if input[8] != '-' || input[13] != '-' || input[18] != '-' || input[23] != '-' {
		err = ErrUuidIsNotValid
		return
	}

	for i, x := range [16]int{
		0, 2, 4, 6,
		9, 11,
		14, 16,
		19, 21,
		24, 26, 28, 30, 32, 34} {
		v, ok := xtob(input[x], input[x+1])
		if !ok {
			err = ErrUuidIsNotValid
			return
		}
		guid[i] = v
	}

	return
}

// xtob converts hex characters x1 and x2 into a byte.
func xtob(x1, x2 byte) (byte, bool) {
	b1 := xvalues[x1]
	b2 := xvalues[x2]
	return (b1 << 4) | b2, b1 != 255 && b2 != 255
}
